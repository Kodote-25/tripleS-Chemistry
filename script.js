// 모달 열기 함수
function openChemistryName(person, chemistryName) {
  const data = chemistryDescriptions[person][chemistryName];

  // 강조 + 링크 변환
  let formattedDescription = data.description.replace(/\*\*(.*?)\*\*/g, (match, p1) => {
    if (data.links && data.links[p1]) {
      return `<a href="${data.links[p1]}" target="_blank" class="highlight">${p1}</a>`;
    }
    return `<span class="highlight">${p1}</span>`;
  });

  document.getElementById("chemistryTitle").innerText = chemistryName;
  document.getElementById("chemistryImage").src = data.image;
  document.getElementById("chemistryDescription").innerHTML = formattedDescription;

  // 링크 표시 (추가 목록)
  const linkDiv = document.getElementById("chemistryLinks");
  linkDiv.innerHTML = "";
  if (data.links) {
    for (const [text, url] of Object.entries(data.links)) {
      // 이미 강조 처리된 건 빼고, 나머지 링크만 보여주기
      if (!data.description.includes(`**${text}**`)) {
        const a = document.createElement("a");
        a.href = url;
        a.target = "_blank";
        a.innerText = text;
        a.style.display = "block";
        linkDiv.appendChild(a);
      }
    }
  }

  // 모달 보이기
  document.getElementById("chemistryModal").style.display = "flex";
}

// 닫기 버튼
const closeBtn = document.querySelector(".close");
if (closeBtn) {
  closeBtn.addEventListener("click", () => {
    document.getElementById("chemistryModal").style.display = "none";
  });
} else {
  console.warn("닫기 버튼(.close)을 찾을 수 없습니다. HTML에 .close 요소가 있는지 확인하세요.");
}

// 바깥 클릭 시 닫기
window.onclick = (event) => {
  const modal = document.getElementById("chemistryModal");
  if (event.target === modal) {
    modal.style.display = "none";
  }
};

// 클릭 이벤트 연결
document.querySelectorAll(".chemistry-item").forEach(item => {
  item.addEventListener("click", () => {
    const person = item.dataset.person;
    const chemistryName = item.dataset.chemistry;
    openChemistryName(person, chemistryName);
  });
});
